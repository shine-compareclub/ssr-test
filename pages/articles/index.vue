          pass away or be diagnosed with a terminal illness.
        </li>
        <li><a
          href="/life-insurance/tpd-insurance/?utm_source=direct&amp;utm_medium=organic&amp;website_source=CompareClub&amp;landing_page=https://compareclub.com.au/"
        >Total and permanent disability (TPD) cover</a>. which pays a lump sum if you can't work again because of
          permanent illness, injury or disability.
        </li>
        <li><a
          href="/life-insurance/income-protection-insurance/?utm_source=direct&amp;utm_medium=organic&amp;website_source=CompareClub&amp;landing_page=https://compareclub.com.au/"
        >Income protection insurance</a>. This pays up to 70% of your usual income if you can't work temporarily due
          to illness or injury.
        </li>
        <li><a
          href="/life-insurance/trauma-insurance/?utm_source=direct&amp;utm_medium=organic&amp;website_source=CompareClub&amp;landing_page=https://compareclub.com.au/"
        >Trauma cover</a>. This pays a lump sum for certain injuries and illnesses, such as cancer and heart attacks.
        </li>
      </ul>
