      ><span class="icon icon-link"></span></a>Can I have multiple life insurance policies?</h3>
      <p>Yes. There are no restrictions in Australia saying that you can only have a single <a
        href="/life-insurance/what-is-life-insurance/?utm_source=direct&amp;utm_medium=organic&amp;website_source=CompareClub&amp;landing_page=https://compareclub.com.au/"
